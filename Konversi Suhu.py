# Program konversi suhu dari Celcius ke Fahrenheit

# Meminta input dari pengguna
celsius = float(input("Masukkan suhu dalam Celcius: "))

# Konversi suhu dari Celcius ke Fahrenheit
fahrenheit = (celsius * 1.8) + 32

# Menampilkan hasil konversi suhu
print(celsius, "derajat Celcius sama dengan", fahrenheit, "derajat Fahrenheit.")
